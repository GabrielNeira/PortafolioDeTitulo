package com.feriantes.portafolio.to;

public class ProductoTO {

	private int idProducto;
	private String nombreProducto;
	private int cantidadProducto;
	private int pesoProducto;
	private int volumenProducto;
	private int estadoProducto;
	private int refrigeracion;
	private String fechaLlegada;
	private int codProductor;
	private String funcion;
	
	public int getIdProducto() {
		return idProducto;
	}
	public void setIdProducto(int idProducto) {
		this.idProducto = idProducto;
	}
	public String getNombreProducto() {
		return nombreProducto;
	}
	public void setNombreProducto(String nombreProducto) {
		this.nombreProducto = nombreProducto;
	}
	public int getCantidadProducto() {
		return cantidadProducto;
	}
	public void setCantidadProducto(int cantidadProducto) {
		this.cantidadProducto = cantidadProducto;
	}
	public int getPesoProducto() {
		return pesoProducto;
	}
	public void setPesoProducto(int pesoProducto) {
		this.pesoProducto = pesoProducto;
	}
	public int getVolumenProducto() {
		return volumenProducto;
	}
	public void setVolumenProducto(int volumenProducto) {
		this.volumenProducto = volumenProducto;
	}
	public int getEstadoProducto() {
		return estadoProducto;
	}
	public void setEstadoProducto(int estadoProducto) {
		this.estadoProducto = estadoProducto;
	}
	public int getRefrigeracion() {
		return refrigeracion;
	}
	public void setRefrigeracion(int refrigeracion) {
		this.refrigeracion = refrigeracion;
	}
	public String getFechaLlegada() {
		return fechaLlegada;
	}
	public void setFechaLlegada(String fechaLlegada) {
		this.fechaLlegada = fechaLlegada;
	}
	public int getCodProductor() {
		return codProductor;
	}
	public void setCodProductor(int codProductor) {
		this.codProductor = codProductor;
	}
	public String getFuncion() {
		return funcion;
	}
	public void setFuncion(String funcion) {
		this.funcion = funcion;
	}

	

	
	


}
